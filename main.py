@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    requested_post = BlogPost.query.get(post_id)
    form = CommentForm()
    is_submit_comment = request.args.get("is_submit_comment")
    if is_submit_comment:
        flash("Comment is posted.")
        is_submit_comment = False

    if form.validate_on_submit():

        new_comment = Comment(
            text=form.comment_text.data,
            # Add parents info
            comment_author=current_user,
            blog_post=requested_post
        )
        db.session.add(new_comment)
        db.session.commit()
        return redirect(url_for("show_post", post_id=post_id,  is_submit_comment=True, current_user=current_user))
    else:
        return render_template("post.html", post=requested_post, form=form, current_user=current_user)


